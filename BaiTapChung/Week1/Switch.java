public class Switch{
    public static void main(String[] args) {
        int menu = 4;
        switch (menu) {
            case 1:
            System.out.println("Ham");
            break;
            case 2:
            System.out.println("Noodle");
            break;
            case 3:
            System.out.println("Pizza");
            break;
            case 4:
            System.out.println("Fries");
            break;
        }
    }
}
